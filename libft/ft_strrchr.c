#include"libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	while (s <= s + i)
		if (s[i--] == c % 256)
			return ((char *) s + i + 1);
	return (NULL);
}
